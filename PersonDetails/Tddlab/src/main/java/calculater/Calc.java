package calculater;

public class Calc {
    public int add(int a, int b){
        return a + b;
    }
    public int subtract(int r, int f){
        return r - f;
    }
    public double multiply(double r, double y){
        return r * y;
    }
    public double divide(double r, double y){
        return r / y;
    }
    public int square(int r){
        return r;
    }
    public double squareRoot(double r){
        return r;
    }
    public double exponents(double r){
        return r;
    }
    public double sineMethod(double r){
        return r;
    }

}
